package chapter2.practice;

/**
 * Write a program that prints three arguments token from the command line. to do
 * this you'll need to index the command-line array of Strings.
 * @author lh
 */
public class CommandArgTest_ch2_10 {
    public static void main(String[] args){
        System.out.println("arg[0] = " + args[0]);
        System.out.println("arg[1] = " + args[1]);
        System.out.println("arg[2] = " + args[2]);
    }
}
